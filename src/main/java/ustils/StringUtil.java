	public static String toHtml(String src) {
		
		String str = src.replaceAll(System.getProperty("line.separator"), "|")	;
		String[] split = str.split("\\|");
		String newStr="";
		for (String string2 : split) {
			newStr+="<p>"+string2+"</p>";
		}
		return newStr;
		
	}
	
	
